    REFERENCES PROPRIETARIO(PRO_ID),
CONSTRAINT FK_FK_MUN_ID FOREIGN KEY (FK_MUN_ID)
    REFERENCES MUNICIPIO(MUN_ID),
CONSTRAINT FK_FK_EST_ID FOREIGN KEY (FK_EST_ID)
    REFERENCES ESTADOO(EST_ID),
CONSTRAINT FK_FK_CAT_ID FOREIGN KEY (FK_CAT_ID)
    REFERENCES MARCA(CAT_ID)
)

