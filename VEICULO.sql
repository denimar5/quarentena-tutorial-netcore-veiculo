CREATE TABLE PROPRIETARIO(

PRO_ID      NUMBER NOT NULL,
PRO_NOME    VARCHAR(80) NOT NULL,
PRO_RG      VARCHAR(15) NOT NULL,
PRO_CPF     VARCHAR(15) NOT NULL,
PRO_EMAIL   VARCHAR(50) NOT NULL,
PRO_DT_NASC      DATE NOT NULL,
PRO_DDD_TELEFONE VARCHAR (15) NOT NULL,
CONSTRAINT PK_PROPRIETARIO PRIMARY KEY (PRO_CPF,PRO_ID)
)

